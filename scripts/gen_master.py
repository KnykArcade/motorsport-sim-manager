def merge_principal_records(records: list[dict]) -> tuple[list[dict], int]:
    merged: dict[str, dict] = {}
    duplicate_count = 0
    numeric_keys = [
        "leadership",
        "technicalVision",
        "operations",
        "driverManagement",
        "commercial",
        "political",
        "riskAppetite",
        "developmentVision",
        "crisisManagement",
        "negotiation",
        "reputation",
    ]
    for rec in records:
        principal_id = rec["principalId"]
        if principal_id not in merged:
            merged[principal_id] = rec
            continue
        duplicate_count += 1
        base = merged[principal_id]
        for key in numeric_keys:
            base[key] = clamp_1_100(weighted_mean([num(base.get(key), 0), num(rec.get(key), 0)]))
        base["careerTimeline"] = list(
            {
                (item["year"], item["series"], item["teamId"], item["role"]): item
                for item in [*base.get("careerTimeline", []), *rec.get("careerTimeline", [])]
            }.values()
        )
        base["careerTimeline"].sort(key=lambda item: (item["year"], item["series"], item["teamId"], item["role"]))
        if rec.get("_sortYear", 0) >= base.get("_sortYear", 0):
            base["contract"] = rec["contract"]
            base["name"] = rec["name"] or base.get("name", "")
            base["principalType"] = rec.get("principalType", base.get("principalType", "Generated"))
            base["_sortYear"] = rec.get("_sortYear", 0)
    for rec in merged.values():
        rec.pop("_sortYear", None)
    return list(merged.values()), duplicate_count


