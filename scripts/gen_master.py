def fold_ascii(text: str) -> str:
    return (
        unicodedata.normalize("NFKD", str(text))
        .encode("ascii", "ignore")
        .decode("ascii")
    )


def norm(text: str) -> str:
    text = fold_ascii(text).lower()
    text = re.sub(r"[^a-z0-9]+", " ", text).strip()
    return re.sub(r"\s+", " ", text)


def slug(text: str) -> str:
    return norm(text).replace(" ", "-")


def num(value, default=None):
    if value is None or (isinstance(value, float) and math.isnan(value)):
        return default
    if isinstance(value, bool):
        return default
    if isinstance(value, (int, float)):
        return float(value)
    s = str(value).replace("\xa0", " ").strip()
    if not s:
        return default
    m = re.search(r"-?\d+(?:\.\d+)?", s)
    if not m:
        return default
    return float(m.group(0))


def as_int_1_100(value, default=50):
    v = num(value, None)
    if v is None:
        return int(default)
    if v <= 10:
        v *= 10
    return int(max(1, min(100, round(v))))


def clamp_1_100(value):
    return int(max(1, min(100, round(value))))


def ts(data, type_name: str) -> str:
    return json.dumps(data, ensure_ascii=False, indent=2) + f" as const satisfies {type_name}"


def ts_file(path: Path, imports: list[str], body: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as fh:
        fh.write("// AUTO-GENERATED by scripts/gen_master.py. Do not hand-edit.\n")
        if imports:
            fh.write("import type {\n")
            for item in imports:
                fh.write(f"  {item},\n")
            fh.write("} from '../../../types/gameTypes';\n\n")
        fh.write(body)
        fh.write("\n")


def norm_series(series: str) -> str:
    s = str(series).lower()
    if "f1" in s:
        return "F1"
    if "cart" in s or "champ car" in s:
        return "CART"
    if "irl" in s or "indycar" in s:
        return "IndyCar"
    return str(series)


def display_series_for_year(year: int, source_series: str) -> str:
    series = norm_series(source_series)
    if series == "CART" and year >= 2004:
        return "Champ Car"
    return series


def module_suffix(series: str) -> str:
    return "ChampCar" if series == "Champ Car" else series


def collect_season_bundle_specs(calendar_df: pd.DataFrame) -> list[tuple[int, str, str]]:
    specs: list[tuple[int, str, str]] = []
    seen: set[tuple[int, str]] = set()
    sorted_rows = calendar_df.sort_values(["Season", "Series", "Round"], kind="stable")
    for _, row in sorted_rows.iterrows():
        season = int(row["Season"])
        source_series = norm_series(row["Series"])
        key = (season, source_series)
        if key in seen:
            continue
        seen.add(key)
        specs.append((season, source_series, display_series_for_year(season, source_series)))
    specs.sort(key=lambda item: (item[0], SERIES_BUNDLE_ORDER.get(item[2], 99), item[1]))
    return specs


def canonical_driver_name(name: str) -> str:
    text = fold_ascii(name).strip()
    text = re.sub(r"\s+", " ", text)
    parts = text.split(" ")
    while parts and re.fullmatch(r"[A-Z]{1,3}", parts[-1].replace(".", "")):
        if parts[-1].replace(".", "") in {"R", "RY", "W", "C", "ROY"}:
            parts.pop()
        else:
            break
    return " ".join(parts).strip()


def wiki_session() -> requests.Session:
    sess = requests.Session()
    sess.headers.update({"User-Agent": USER_AGENT})
    return sess


def wiki_search(sess: requests.Session, query: str, limit: int = 5) -> list[dict]:
    r = sess.get(
        WIKI_API,
        params={"action": "query", "list": "search", "srsearch": query, "format": "json", "utf8": 1, "srlimit": limit},
        timeout=30,
    )
    r.raise_for_status()
    return r.json().get("query", {}).get("search", [])


def wiki_page_url(title: str) -> str:
    return WIKI_PAGE + quote(title.replace(" ", "_"))


def wiki_page_html(sess: requests.Session, title: str) -> str:
    r = sess.get(wiki_page_url(title), timeout=30)
    r.raise_for_status()
    return r.text


def wiki_extract_name_from_html(html: str) -> str | None:
    soup = BeautifulSoup(html, "html.parser")
    labels = ["full name", "birth name", "name"]
    for tr in soup.select("table.infobox tr"):
        th = tr.find("th")
        if not th:
            continue
        label = th.get_text(" ", strip=True).lower()
        if label not in labels:
            continue
        td = tr.find("td")
        if not td:
            continue
        text = td.get_text(" ", strip=True)
        text = re.sub(r"\[[^\]]+\]", "", text)
        text = re.sub(r"\s+", " ", text).strip()
        if text and not re.search(r"^[A-Z]\.?(\s*[A-Z]\.?)+\s+[A-Z][a-z]", text):
            return fold_ascii(text)
    for p in soup.select("div.mw-parser-output > p"):
        text = p.get_text(" ", strip=True)
        text = re.sub(r"\[[^\]]+\]", "", text)
        if not text:
            continue
        head = re.split(r"\s*\(|,\s*known as\b|,\s*nicknamed\b|,\s*also known as\b", text, maxsplit=1)[0].strip()
        head = re.sub(r"\s*\"[^\"]+\"\s*", " ", head)
        head = re.sub(r"\s+", " ", head).strip(" ,;")
        if head and re.search(r"[A-Z][a-z]", head) and not re.search(r"^[A-Z]\.?(\s*[A-Z]\.?)+(\s|$)", head):
            return fold_ascii(head)
    return None


