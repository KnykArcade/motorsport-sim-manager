def merge_year_ranges(years: list[int]) -> list[dict]:
    if not years:
        return []
    ranges = []
    start = prev = years[0]
    for year in years[1:]:
        if year == prev + 1:
            prev = year
            continue
        ranges.append({"start": start, "end": prev})
        start = prev = year
    ranges.append({"start": start, "end": prev})
    return ranges


def build_team_record(lineage_rows: pd.DataFrame, team_id: str, series: str, change_rows: pd.DataFrame | None = None) -> dict:
    lineage_rows = lineage_rows.sort_values(["Season", "Team_Name_That_Season"], kind="stable")
    canonical = str(lineage_rows.iloc[0]["Canonical_Team_Name"])
    periods = []
    events = []
    for _, row in lineage_rows.iterrows():
        periods.append(
            {
                "fromYear": int(row["Season"]),
                "toYear": int(row["Season"]),
                "name": str(row["Team_Name_That_Season"]),
            }
        )
    prev = None
    for _, row in lineage_rows.iterrows():
        current = str(row["Team_Name_That_Season"])
        if prev is not None and prev != current:
            events.append({"year": int(row["Season"]), "oldName": prev, "newName": current, "note": "Season rename"})
        prev = current
    if change_rows is not None and not change_rows.empty:
        for _, row in change_rows.sort_values(["Year", "From_Name", "To_Name"], kind="stable").iterrows():
            event = {"year": int(row["Year"]), "newName": str(row["To_Name"]), "note": str(row.get("Notes") or row.get("Event_Type") or "Workbook change event")}
            if pd.notna(row.get("From_Name")) and str(row.get("From_Name")).strip():
                event["oldName"] = str(row.get("From_Name"))
            if pd.notna(row.get("Event_Type")) and str(row.get("Event_Type")).strip():
                event["note"] = f'{str(row.get("Event_Type"))}: {event["note"]}'
            events.append(event)
    deduped_events = []
    seen_events = set()
    for event in sorted(events, key=lambda e: (e.get("year", 0), e.get("oldName", ""), e.get("newName", ""), e.get("note", ""))):
        key = (event.get("year"), event.get("oldName"), event.get("newName"), event.get("note"))
        if key in seen_events:
            continue
        seen_events.add(key)
        deduped_events.append(event)
    events = deduped_events
    budget_vals = [as_int_1_100(v) for v in lineage_rows.get("Development Budget", pd.Series(dtype=float)).dropna().tolist()]
    sponsor_vals = [as_int_1_100(v) for v in lineage_rows.get("Sponsor Strength", pd.Series(dtype=float)).dropna().tolist()]
    performance_vals = [as_int_1_100(v) for v in lineage_rows.get("Performance_Score", pd.Series(dtype=float)).dropna().tolist()]
    overall_vals = [as_int_1_100(v) for v in lineage_rows.get("Overall", pd.Series(dtype=float)).dropna().tolist()]
    pit_vals = [as_int_1_100(v) for v in lineage_rows.get("Pit Crew/Operations", pd.Series(dtype=float)).dropna().tolist()]
    reliability_vals = [as_int_1_100(v) for v in lineage_rows.get("Reliability", pd.Series(dtype=float)).dropna().tolist()]
    race_vals = [as_int_1_100(v) for v in lineage_rows.get("Race Pace", pd.Series(dtype=float)).dropna().tolist()]
    eng_vals = [as_int_1_100(v) for v in lineage_rows.get("Engine Power", pd.Series(dtype=float)).dropna().tolist()]
    aero_vals = [as_int_1_100(v) for v in lineage_rows.get("Aero Efficiency", pd.Series(dtype=float)).dropna().tolist()]
    mech_vals = [as_int_1_100(v) for v in lineage_rows.get("Mechanical Grip", pd.Series(dtype=float)).dropna().tolist()]
    reputation = clamp_1_100(weighted_mean(overall_vals or performance_vals or [70]))
    team = {
        "teamLineageId": team_id,
        "series": series,
        "canonicalName": canonical,
        "namePerPeriod": periods,
        "nameChangeEvents": events,
        "reputation": reputation,
        "raceOperations": clamp_1_100(weighted_mean(pit_vals or reliability_vals or [reputation])),
        "pitCrewOperations": clamp_1_100(weighted_mean(pit_vals or reliability_vals or [reputation])),
        "developmentRate": clamp_1_100(weighted_mean([reputation, clamp_1_100(mean(budget_vals or [60]))])),
        "facilities": clamp_1_100(weighted_mean([reputation, clamp_1_100(mean(race_vals or [60]))])),
        "sponsorStrength": clamp_1_100(weighted_mean(sponsor_vals or [reputation])),
        "commercialStrength": clamp_1_100(weighted_mean([reputation] + sponsor_vals) if sponsor_vals else reputation),
        "politicalInfluence": clamp_1_100(weighted_mean([reputation, clamp_1_100(mean(performance_vals or [reputation]))])),
        "financeHealth": clamp_1_100(weighted_mean([clamp_1_100(mean(budget_vals or [60])), reputation])),
        "budget": int(round(5_000_000 + reputation * 250_000)),
    }
    # Use the car-related season signals if they exist.
    if eng_vals:
        team["reputation"] = clamp_1_100(weighted_mean([team["reputation"], mean(eng_vals)]))
    if aero_vals:
        team["facilities"] = clamp_1_100(weighted_mean([team["facilities"], mean(aero_vals)]))
    if mech_vals:
        team["raceOperations"] = clamp_1_100(weighted_mean([team["raceOperations"], mean(mech_vals)]))
    return team


def build_supplier_records(supplier_df: pd.DataFrame, team_history: pd.DataFrame, car_by_team: dict[tuple[int, str, str], dict]) -> list[dict]:
    out = []
    for supplier_id, group in supplier_df.groupby("Supplier_ID", dropna=False):
        group = group.sort_values(["First_Year", "Last_Year", "Supplier_Name"], kind="stable")
        supplier_name = str(group.iloc[0]["Supplier_Name"])
        series_list = sorted({
            display_series_for_year(int(row["Last_Year"]), norm_series(x))
            for _, row in group.iterrows()
            for value in [row.get("Series_List", "")]
            for x in str(value).split(",")
            if str(x).strip()
        })
        year_values = sorted({int(y) for col in ["First_Year", "Last_Year"] for y in group[col].dropna().astype(int).tolist()})
        years = merge_year_ranges(year_values)
        matches = []
        supplier_name_norms = {norm(str(v)) for v in group["Supplier_Name"].dropna().tolist() if str(v).strip()}
        for _, team_row in team_history.iterrows():
            text_fields = [str(team_row.get("Engine_or_Power_Unit", "")), str(team_row.get("Chassis", ""))]
            if any(any(sn and (sn in norm(text) or norm(text) in sn) for sn in supplier_name_norms) for text in text_fields):
                matches.append(team_row)
        if not matches:
            matches = [r for _, r in team_history.iterrows() if norm_series(r["Series"]) in series_list]
        power_vals, rel_vals, eff_vals = [], [], []
        for team_row in matches[:200]:
            key = (int(team_row["Season"]), norm_series(team_row["Series"]), norm(str(team_row["Canonical_Team_Name"])))
            car = car_by_team.get(key)
            if car:
                power_vals.append(car["enginePower"])
                rel_vals.append(car["reliability"])
                eff_vals.append(clamp_1_100(weighted_mean([car["downforce"], car["mechanicalGrip"]])))
            elif not pd.isna(team_row.get("Overall")):
                power_vals.append(as_int_1_100(team_row.get("Overall")))
                rel_vals.append(as_int_1_100(team_row.get("Reliability")))
                eff_vals.append(as_int_1_100(team_row.get("Aero Efficiency")))
        first_row = group.iloc[0]
        out.append(
            {
                "supplierId": str(supplier_id),
                "type": "chassis" if str(first_row["Supplier_Type"]).lower().startswith("chassis") else "engine",
                "name": supplier_name,
                "seriesList": sorted(set(series_list)),
                "years": years,
                "power": clamp_1_100(weighted_mean(power_vals or [60])),
                "reliability": clamp_1_100(weighted_mean(rel_vals or [60])),
                "efficiency": clamp_1_100(weighted_mean(eff_vals or [60])),
            }
        )
    return out


