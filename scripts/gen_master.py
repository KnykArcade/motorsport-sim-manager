def wiki_expand_display_name(name: str, series: str | None = None) -> str:
    cache_key = f"{norm(name)}|{norm(series or '')}"
    if cache_key in _WIKI_NAME_CACHE:
        return _WIKI_NAME_CACHE[cache_key]
    override = DISPLAY_NAME_OVERRIDES.get(norm(name))
    if override:
        expanded = fold_ascii(override)
        _WIKI_NAME_CACHE[cache_key] = expanded
        return expanded
    if not re.search(r"\b[A-Z]\.?(\s*[A-Z]\.?)+\b", name) and not re.search(r"\b[A-Z]\.\s+[A-Z]", name):
        expanded = fold_ascii(name)
        _WIKI_NAME_CACHE[cache_key] = expanded
        return expanded
    sess = wiki_session()
    queries = [name]
    if series:
        queries.extend([f"{name} racing driver", f"{name} {series} racing driver"])
    for query in queries:
        try:
            results = wiki_search(sess, query)
        except Exception:
            continue
        ranked = sorted(
            results[:5],
            key=lambda result: (
                0 if norm(result.get("title", "")) == norm(name) else 1,
                0 if "born" in result.get("snippet", "").lower() else 1,
                0 if "racing driver" in result.get("snippet", "").lower() else 1,
            ),
        )
        for result in ranked:
            title = result.get("title")
            if not title:
                continue
            try:
                html = wiki_page_html(sess, title)
            except Exception:
                continue
            expanded = wiki_extract_name_from_html(html)
            if expanded and expanded != fold_ascii(name):
                _WIKI_NAME_CACHE[cache_key] = expanded
                return expanded
    expanded = fold_ascii(name)
    _WIKI_NAME_CACHE[cache_key] = expanded
    return expanded


def weighted_mean(values: list[float], weights: list[float] | None = None) -> float:
    if not values:
        return 50.0
    if not weights:
        return sum(values) / len(values)
    total = sum(weights)
    if total <= 0:
        return sum(values) / len(values)
    return sum(v * w for v, w in zip(values, weights)) / total


