def oval_subtype_from_length(length_km: float) -> str:
    miles = float(length_km) / 1.609344 if length_km else 0.0
    if miles < 1.0:
        return "Short Oval"
    if miles <= 2.0:
        return "Speedway"
    return "Superspeedway"


def oval_subtype_from_subcategory(subcategory: str, length_km: float | None = None) -> str | None:
    text = norm(str(subcategory))
    if "superspeedway" in text:
        return "Superspeedway"
    if "intermediate" in text or "speedway" in text:
        return "Speedway"
    if "short" in text or "flat" in text:
        return "Short Oval"
    if length_km is not None:
        return oval_subtype_from_length(length_km)
    return None


def track_match_keys(text: str) -> list[str]:
    raw = str(text or "").strip()
    if not raw:
        return []
    values = [norm(raw)]
    stripped = re.sub(r"\s*\([^)]*\)\s*", " ", raw)
    if stripped.strip() != raw:
        values.append(norm(stripped))
    stripped_years = re.sub(r"\b\d{4}\s*[-/]\s*\d{4}\b", " ", stripped)
    if stripped_years.strip() not in {"", stripped.strip()}:
        values.append(norm(stripped_years))
    return [v for v in dict.fromkeys(values) if v]


def track_fill_keys(text: str) -> list[str]:
    raw = str(text or "").strip()
    if not raw:
        return []
    variants = [norm(raw)]
    base = re.sub(r"\s*\([^)]*\)\s*", " ", raw)
    if base.strip() != raw:
        variants.append(norm(base))
    if "/" in raw:
        variants.append(norm(raw.split("/")[0]))
    if " - " in raw:
        variants.append(norm(raw.split(" - ")[0]))
    return [v for v in dict.fromkeys(variants) if v]


