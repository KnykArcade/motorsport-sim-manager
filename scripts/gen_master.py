def build_driver_record(
    name: str,
    rows: list[pd.Series],
    timeline_entries: list[tuple[int, str, str]],
    nationalities: list[str],
    registry_match: dict | None,
    scraped_match: dict | None,
) -> dict:
    canonical = canonical_driver_name(name)
    driver_id = str(registry_match["Driver ID"]) if registry_match and registry_match.get("Driver ID") else f"driver-{slug(canonical)}"
    source_display = str(registry_match["Driver Name"]) if registry_match and registry_match.get("Driver Name") else canonical
    display = wiki_expand_display_name(source_display, None)
    dob_value, birth_year_value, dob_source = resolve_driver_dob(registry_match, scraped_match)
    skills = defaultdict(list)
    for row in rows:
        for key in [
            "cornering",
            "braking",
            "straights",
            "tractionAcceleration",
            "elevationBlindCorners",
            "technical",
            "overtakingRacecraft",
            "surfaceGripBumpiness",
            "riskManagement",
            "enduranceConsistency",
            "qualifying",
            "racePace",
            "adaptability",
            "aggression",
            "composure",
        ]:
            skills[key].append(as_int_1_100(row.get(key)))
    driver = {k: clamp_1_100(weighted_mean(v)) for k, v in skills.items()}
    driver["startsRestarts"] = clamp_1_100(weighted_mean([driver["aggression"], driver["overtakingRacecraft"]]))
    driver["wetWeather"] = clamp_1_100(weighted_mean([driver["adaptability"], driver["surfaceGripBumpiness"], driver["riskManagement"]]))
    driver["tireManagement"] = clamp_1_100(weighted_mean([driver["enduranceConsistency"], driver["composure"], driver["riskManagement"]]))
    driver["pressureHandling"] = clamp_1_100(weighted_mean([driver["composure"], driver["riskManagement"], driver["racePace"]]))
    driver["feedbackQuality"] = clamp_1_100(weighted_mean([driver["technical"], driver["adaptability"], driver["composure"]]))
    driver["technicalUnderstanding"] = clamp_1_100(weighted_mean([driver["technical"], driver["cornering"], driver["braking"]]))
    driver["mechanicalSympathy"] = clamp_1_100(weighted_mean([driver["riskManagement"], driver["enduranceConsistency"], driver["composure"]]))
    driver["overall"] = derive_driver_overall(driver)
    driver["potential"] = clamp_1_100(driver["overall"] + 8)
    driver["developmentPotential"] = clamp_1_100(120 - driver["overall"])
    driver["morale"] = clamp_1_100(55 + driver["overall"] / 5)
    driver["trust"] = clamp_1_100(50 + driver["overall"] / 4)
    driver["reputation"] = clamp_1_100(driver["overall"])
    driver["marketValue"] = clamp_1_100(35 + driver["overall"] / 2)
    unique_timeline = []
    seen_timeline = set()
    for year, series, team_id in sorted(timeline_entries, key=lambda item: (item[0], item[1], item[2])):
        key = (year, series, team_id)
        if key in seen_timeline:
            continue
        seen_timeline.add(key)
        unique_timeline.append((year, series, team_id))
    driver["contract"] = {
        "teamId": unique_timeline[0][2] if unique_timeline else "",
        "yearsLeft": 1,
        "salary": clamp_1_100(driver["overall"] / 2),
        "options": [],
    }
    record = {
        "driverId": driver_id,
        "name": display,
        "traits": registry_traits_to_list(registry_match or {}, dob_value),
        "seriesAffinity": registry_series_affinity(registry_match or {}, unique_timeline),
        "careerTimeline": [
            {"year": year, "series": series, "teamId": team_id, "role": "driver"}
            for year, series, team_id in unique_timeline
        ],
        **driver,
    }
    if dob_value:
        record["dateOfBirth"] = dob_value
        if birth_year_value:
            record["birthYear"] = birth_year_value
    elif birth_year_value:
        record["birthYear"] = birth_year_value
    if registry_match:
        nationality = registry_match.get("Nationality")
        if nationality is not None and not pd.isna(nationality) and str(nationality).strip():
            record["nationality"] = str(nationality)
    elif nationalities:
        nationality = next((n for n in nationalities if n), "")
        if nationality:
            record["nationality"] = nationality
    if dob_source == "scraped" and "dateOfBirth" in record and "birthYear" not in record and birth_year_value:
        record["birthYear"] = birth_year_value
    return record


def merge_driver_records(records: list[dict]) -> tuple[list[dict], int]:
    merged: dict[str, dict] = {}
    duplicate_count = 0
    numeric_keys = [
        "cornering",
        "braking",
        "straights",
        "tractionAcceleration",
        "elevationBlindCorners",
        "technical",
        "overtakingRacecraft",
        "surfaceGripBumpiness",
        "riskManagement",
        "enduranceConsistency",
        "qualifying",
        "racePace",
        "startsRestarts",
        "wetWeather",
        "tireManagement",
        "adaptability",
        "aggression",
        "composure",
        "pressureHandling",
        "feedbackQuality",
        "technicalUnderstanding",
        "mechanicalSympathy",
        "overall",
        "potential",
        "developmentPotential",
        "morale",
        "trust",
        "reputation",
        "marketValue",
    ]
    for rec in records:
        driver_id = rec["driverId"]
        if driver_id not in merged:
            merged[driver_id] = rec
            continue
        duplicate_count += 1
        base = merged[driver_id]
        for key in numeric_keys:
            base[key] = clamp_1_100(weighted_mean([num(base.get(key), 0), num(rec.get(key), 0)]))
        base["traits"] = list(dict.fromkeys([*(base.get("traits", [])), *(rec.get("traits", []))]))
        base["careerTimeline"] = list({
            (item["year"], item["series"], item["teamId"], item["role"]): item
            for item in [*base.get("careerTimeline", []), *rec.get("careerTimeline", [])]
        }.values())
        base["careerTimeline"].sort(key=lambda item: (item["year"], item["series"], item["teamId"]))
        if not base.get("nationality") and rec.get("nationality"):
            base["nationality"] = rec["nationality"]
        if not base.get("dateOfBirth") and rec.get("dateOfBirth"):
            base["dateOfBirth"] = rec["dateOfBirth"]
        if not base.get("birthYear") and rec.get("birthYear"):
            base["birthYear"] = rec["birthYear"]
        if rec.get("seriesAffinity"):
            existing = {(item["series"], item["strength"]) for item in base.get("seriesAffinity", [])}
            for item in rec["seriesAffinity"]:
                existing.add((item["series"], item["strength"]))
            base["seriesAffinity"] = [{"series": s, "strength": st} for s, st in sorted(existing)]
    return list(merged.values()), duplicate_count


