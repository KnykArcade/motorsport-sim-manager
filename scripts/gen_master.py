def registry_traits_to_list(row: dict, dob_value) -> list[str]:
    traits: list[str] = []

    def add(value: str | float | None, separators: str = r"[|;/]") -> None:
        if value is None or pd.isna(value):
            return
        for token in split_tokens(str(value), separators=separators):
            if token and token not in traits:
                traits.append(token)

    add(row.get("Career Tier"))
    add(row.get("Racing Archetype"), separators=r"[|;/]")
    add(row.get("Racing Traits"), separators=r"[|;/]")
    add(row.get("Personality Traits"), separators=r"[|;/]")
    add(row.get("Racecraft Strengths"), separators=r"[|;/]")
    add(row.get("Development / Management Risks"), separators=r"[|;/]")
    add(row.get("Game Trait Tags"), separators=r"[|]")
    if dob_value is not None and not pd.isna(dob_value):
        month = pd.to_datetime(dob_value).month
        traits.append("Early Bloomer" if month <= 6 else "Late Bloomer")
    return traits


def registry_series_affinity(row: dict, timeline_entries: list[tuple[int, str, str]]) -> list[dict]:
    values: list[dict] = []
    series_tags = []
    if row.get("Series Tags") is not None and not pd.isna(row.get("Series Tags")):
        series_tags = [norm_series(tag) for tag in split_tokens(str(row.get("Series Tags")), separators=r"[|,/]")]
    if not series_tags:
        series_tags = sorted({series for _, series, _ in timeline_entries})
    for series in series_tags:
        if series:
            values.append({"series": norm_series(series), "strength": 100})
    if not values and timeline_entries:
        counts = defaultdict(int)
        for _, series, _ in timeline_entries:
            counts[series] += 1
        total = sum(counts.values())
        if total:
            values = [{"series": series, "strength": round(count / total * 100)} for series, count in counts.items()]
    return values


def resolve_driver_dob(registry_match: dict | None, scraped_match: dict | None) -> tuple[str | None, int | None, str]:
    if registry_match:
        dob = registry_match.get("Date of Birth")
        if dob is not None and not pd.isna(dob):
            parsed = pd.to_datetime(dob)
            return parsed.date().isoformat(), int(parsed.year), "registry"
    if scraped_match:
        birth_date = scraped_match.get("birth_date")
        birth_year = scraped_match.get("birth_year")
        if birth_date is not None and not pd.isna(birth_date) and str(birth_date).strip():
            parsed = pd.to_datetime(birth_date, errors="coerce")
            if not pd.isna(parsed):
                return parsed.date().isoformat(), int(parsed.year), "scraped"
        if birth_year is not None and not pd.isna(birth_year):
            return None, int(float(birth_year)), "scraped"
    return None, None, ""


def driver_gap_bucket(timeline_entries: list[tuple[int, str, str]]) -> str:
    series_years: dict[str, list[int]] = defaultdict(list)
    for year, series, _ in timeline_entries:
        series_years[series].append(year)
    if series_years.get("CART"):
        return "CART"
    indy_years = series_years.get("IndyCar", [])
    if indy_years and min(indy_years) < 2008:
        return "IndyCar pre-2008"
    return "F1"


