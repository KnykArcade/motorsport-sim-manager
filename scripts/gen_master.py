def build_track_lookups(track_df: pd.DataFrame) -> tuple[dict[str, dict], dict[str, dict]]:
    tracks: dict[str, dict] = {}
    alias_map: dict[str, dict] = {}
    for _, row in track_df.iterrows():
        track_id = str(row["Track_ID"])
        name = str(row["Track_Name_For_Game"])
        rec = {
            "trackId": track_id,
            "name": name,
            "facility": None if pd.isna(row.get("Facility")) else str(row.get("Facility")),
            "configNote": None if pd.isna(row.get("Config_Note")) else str(row.get("Config_Note")),
            "type": track_type_from_text(str(row.get("Track_Type", ""))),
            "location": None if pd.isna(row.get("Location")) else str(row.get("Location")),
            "lengthKm": float(row["Length_km"]),
            "seasonsUsed": [],
            "aliases": [],
            "attributes": None,
            "demandProfile": None,
        }
        tracks[track_id] = rec
        alias_map[norm(name)] = rec
        if not pd.isna(row.get("Aliases_Found_In_Calendars")):
            aliases = [a.strip() for a in str(row["Aliases_Found_In_Calendars"]).split(";") if a.strip()]
            for a in aliases:
                alias_map[norm(a)] = rec
                rec["aliases"].append(a)
    return tracks, alias_map


def build_team_alias_map(team_alias_df: pd.DataFrame) -> dict[str, str]:
    mapping: dict[str, str] = {}
    raw_col = next(c for c in team_alias_df.columns if "Raw" in c or "Team_Name" in c)
    canonical_col = next(c for c in team_alias_df.columns if "Canonical" in c)
    for _, row in team_alias_df.iterrows():
        raw = row.get(raw_col)
        canonical = row.get(canonical_col)
        if pd.isna(raw) or pd.isna(canonical):
            continue
        mapping[norm(str(raw))] = str(canonical)
    return mapping


def split_tokens(text: str, separators: str = r"[|;/]") -> list[str]:
    out = []
    for part in re.split(separators, str(text)):
        clean = re.sub(r"\s+", " ", str(part).strip())
        if clean:
            out.append(clean)
    return out


def is_placeholder_driver_name(name: str) -> bool:
    return norm(name) in PLACEHOLDER_DRIVER_NAMES


def split_driver_components(name: str) -> list[str]:
    raw = str(name).strip()
    if not raw or is_placeholder_driver_name(raw):
        return []
    parts = split_tokens(raw, separators=r"[/]")
    if not parts:
        parts = [raw]
    cleaned = []
    for part in parts:
        candidate = canonical_driver_name(part)
        if candidate and not is_placeholder_driver_name(candidate):
            cleaned.append(candidate)
    return cleaned


def initials_aliases(full_name: str) -> set[str]:
    toks = norm(full_name).split()
    if len(toks) < 2:
        return set()
    surname = toks[-1]
    given = toks[:-1]
    initials = "".join(token[0] for token in given if token)
    spaced_initials = " ".join(token[0] for token in given if token)
    aliases = {
        f"{initials} {surname}".strip(),
        f"{spaced_initials} {surname}".strip(),
        f"{initials}{surname}".strip(),
        f"{spaced_initials}".strip(),
    }
    # Also include one- and two-initial forms for hyphenated/given-name variants.
    if len(given) >= 1:
        aliases.add(f"{given[0][0]} {surname}".strip())
    if len(given) >= 2:
        aliases.add(f"{given[0][0]}{given[1][0]} {surname}".strip())
    return {alias for alias in aliases if alias}


def build_driver_registry_lookup(driver_df: pd.DataFrame) -> dict[str, dict]:
    lookup: dict[str, dict] = {}
    for _, row in driver_df.iterrows():
        rec = row.to_dict()
        fields = [str(rec.get(col, "")) for col in ["Driver Name", "Normalized Name", "Name Variants"]]
        if any("/" in field for field in fields if field):
            continue
        primary = str(rec.get("Normalized Name", "")).strip()
        if primary:
            lookup.setdefault(norm(primary), rec)
        for col in ["Driver Name", "Name Variants"]:
            value = rec.get(col)
            if pd.isna(value) or not str(value).strip():
                continue
            for alias in split_tokens(str(value), separators=r"[|;/]"):
                lookup.setdefault(norm(alias), rec)
                for alt in initials_aliases(alias):
                    lookup.setdefault(norm(alt), rec)
        for alias in initials_aliases(str(rec.get("Driver Name", ""))):
            lookup.setdefault(norm(alias), rec)
    return lookup


def build_scraped_dob_lookup(scraped_df: pd.DataFrame) -> dict[str, dict]:
    lookup: dict[str, dict] = {}
    if scraped_df.empty:
        return lookup
    for _, row in scraped_df.iterrows():
        rec = row.to_dict()
        keys = {
            norm(str(rec.get("canonical_name", ""))),
            norm(str(rec.get("driver_id", ""))),
        }
        series = str(rec.get("series", "")).strip()
        if series:
            keys.add(f"{norm(str(rec.get('canonical_name', '')))}|{norm(series)}")
        for key in keys:
            if key:
                lookup.setdefault(key, rec)
    return lookup


